// Runs as an npm "prebuild"/"prestart" hook. Stamps the actual commit being
// built into the bundle, so the deployed app can be checked against git
// instead of trusting that a push reached Vercel.
const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

function sh(cmd, fallback) {
  try {
    return execSync(cmd, { encoding: "utf8" }).trim();
  } catch {
    return fallback;
  }
}

const commit = process.env.VERCEL_GIT_COMMIT_SHA || sh("git rev-parse HEAD", "unknown");
const branch = process.env.VERCEL_GIT_COMMIT_REF || sh("git rev-parse --abbrev-ref HEAD", "unknown");
const builtAt = new Date().toISOString();

const out = `// AUTO-GENERATED by scripts/generate-build-info.js — do not edit by hand.
export const BUILD_INFO = {
  commit: ${JSON.stringify(commit)},
  branch: ${JSON.stringify(branch)},
  builtAt: ${JSON.stringify(builtAt)},
};
`;

fs.writeFileSync(path.join(__dirname, "..", "src", "buildInfo.generated.ts"), out);
console.log(`[build-info] commit=${commit.slice(0, 8)} branch=${branch} builtAt=${builtAt}`);
